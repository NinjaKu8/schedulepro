export function DoodCalendarDay({ date, dayOfWeek, disabled, isSelected }: Props): JSX.Element {
  const show_types = useAppSelector(state=>state.local.sch_dood_show_types)

  // in lieu of a redux selector
  const todayWorkElements = elements.filter(element=>{
    const row = rows.find(r=>r._id===element._id)
    if(!row) return false
    const column = row.columns.some(c=>{
      return isDateSame(dateToISO(c.date),date) && show_types.reduce<boolean>((acc,cur)=>{
        // @ts-ignore
        if(c[cur]) return true
        return acc
      },false)
    })
    return column
  })

  return (
    <CalendarDay
      date={date}
      disabled={disabled}
      isSelected={isSelected}
      isSelectedDayOfWeek={dayOfWeek!==undefined ? [0,6].includes(dayOfWeek) : false}
    >
      <div className='d-flex align-items-center justify-content-between'>
        {date!==undefined && dateFormat(date,'d')}
        {/* <FaArrowRight /> */}
      </div>
      <div className='d-flex flex-wrap justify-content-center gap-1 small'>
        {todayWorkElements.map(element=>(
          <div key={element._id} className='px-2 bg-primary-25 rounded'>{element.name}</div>
        ))}
        {/* <FlagText>S</FlagText>
        <FlagText isFlagged tipText="George can't work on 1/12/23">W</FlagText>
        <FlagText>T</FlagText>
        <FlagText>F</FlagText> */}
      </div>
    </CalendarDay>
  )
}
