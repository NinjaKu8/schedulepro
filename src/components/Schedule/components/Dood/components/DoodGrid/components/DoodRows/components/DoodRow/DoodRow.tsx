export function DoodRow({ elementId }: Props): JSX.Element | null {
  const row = rows.find((r: IDoodRow)=>r._id===elementId) // pretending to be redux call
  const { t } = useTranslation()
  return (
    row
      ? <div className='dood__row d-flex'>
          <DoodElement elementId={row._id} />

          {row.columns?.map((status,i)=>(
            <DoodCalendarEvents 
              key={i}
              date={status.date} 
              elementId={elementId} 
              isStarted={status.isStarted}
              hasWrapped={status.hasWrapped}
              isInDrop={status.isInDrop}
            >
              {(isDayOff, calendarEventsText, isFlagged) => (
                <DoodCell isDayOff={isDayOff} className={classnames({
                  'status-start': status.isStart,
                  'status-work': status.isWork,
                  'status-finish': status.isFinish,
                  'status-hold': status.isHold,
                  'status-drop': status.isDrop,
                  'status-pickup': status.isPickup,
                  'status-dayoff-active': isDayOff && status.isStarted && !status.isInDrop && !status.hasWrapped
                })}>
                  <DoodStatus 
                    status={status} 
                    calendarEventsText={calendarEventsText} 
                    isFlagged={isFlagged} 
                    tipText={`George ${t('is unavailable on')} 3/16`} 
                  />
                </DoodCell> 
              )}
            </DoodCalendarEvents>
          ))}
          
          {row.totals?.map((total,i)=>(
            <DoodTotal key={i} total={total} />        
          ))}
        </div>
      : null
  )
}
